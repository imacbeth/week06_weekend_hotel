package Hotel;

public class Guest {

    private String name;
    private double wallet;

    public Guest(String name, double wallet) {
        this.name = name;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }


    public double getWallet() {
        return this.wallet;
    }



}